// function change Color
function myBoxPink (){
    document.getElementById('box').style.backgroundColor=('#d63384')
    document.getElementById('text').innerHTML='PINK COLOR'
}
function myBoxRed () {
    document.getElementById('box').style.backgroundColor = ('#ff0000');
    document.getElementById('text').innerHTML='RED COLOR'
}
function myBoxWhite(){
    document.getElementById('box').style.backgroundColor = ('white');
    document.getElementById('text').innerHTML='WHITE COLOR'
}

